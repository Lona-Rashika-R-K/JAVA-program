public class bitwise {
    public static void main(String[] args) {
        int a=423;
        int b=526;
        System.out.println(a^b);
        System.out.println(a&b);
        System.out.println(a|b);
        int c=943;
        System.out.println(c<<3);
        System.out.println(c>>5);
        int d=2593;
        System.out.println(d<<2);
        System.out.println(d>>6);
    }
}
